#!/bin/bash
# سكريبت إعادة تعيين المشروع

echo "🔄 إعادة تعيين مشروع Nexus"
echo "=========================="

# حذف البيئة الافتراضية
if [ -d "venv" ]; then
    echo "🗑️ حذف البيئة الافتراضية..."
    rm -rf venv
fi

# حذف السجلات
if [ -d "logs" ]; then
    echo "🗑️ حذف السجلات..."
    rm -rf logs/*
fi

# حذف قاعدة البيانات المحلية (إن وجدت)
if [ -f "test.db" ]; then
    echo "🗑️ حذف قاعدة البيانات المحلية..."
    rm -f test.db
fi

# حذف الملفات المرفوعة
if [ -d "uploads" ]; then
    echo "🗑️ حذف الملفات المرفوعة..."
    rm -rf uploads/*
fi

echo "✅ تم إعادة التعيين بنجاح"
